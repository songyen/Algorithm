import java.util.Scanner;

public class CodeUp100_N1071 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);;

        while(sc.hasNext()){
            int a = sc.nextInt();
            if(a==0)
                break;
            else
                System.out.println(a);
        }
        sc.close();


    }
}
